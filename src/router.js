class Router {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////
  _root;
  _routes;
  _ajaxService;

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  constructor(root) {
    this._root = root;
    this._routes = {};

  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  addRoute(route) {
    this._routes[route.name] = route;
  }

  loadRoute(name){
    let route = this._routes[name];
    if (route) {
      this._root.innerHTML = '';

      let elem = document.createElement('div');
      elem.innerHTML = name;
      this._root.appendChild(elem);
    }
  }

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default Router;